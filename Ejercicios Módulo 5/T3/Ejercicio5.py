import numpy as np

def matriz_resultados():
    arrayOne = np.array([[5, 6, 9], [21, 18, 27]])
    arrayTwo = np.array([[15, 33, 24], [4, 7, 1]])
    
    matriz_suma = arrayOne + arrayTwo

    matriz_cuadrado = np.square(matriz_suma)
    
    return matriz_cuadrado

if __name__ == "__main__":
    print(matriz_resultados())












































































































































































































